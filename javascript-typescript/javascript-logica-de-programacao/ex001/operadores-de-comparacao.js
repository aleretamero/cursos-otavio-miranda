/*
 Operadores de comparação
 >   → maior
 >=  → maior ou igual
 <   → menor
 <=  → menor ou igual
 ==  → igualdade (valor)
 === → igualdade estrita (valor e tipo)
 !=  → diferente (valor)
 !== → diferente estrita (valor e tipo)
*/

const num1 = 10;
const num2 = "10";
const comp = num1 == num2;
console.log(comp);